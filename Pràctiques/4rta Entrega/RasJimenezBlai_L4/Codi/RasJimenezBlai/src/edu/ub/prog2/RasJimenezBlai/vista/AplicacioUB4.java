        DefaultListModel model = new DefaultListModel();
        model.clear();
        List<String> llista;
        if (!titol.equals("")) {
            try {
                llista = controlador.mostrarAlbum(titol);
                Iterator itr = llista.iterator();
                while (itr.hasNext()) {
                    model.addElement(itr.next());
                }
            } catch (AplicacioException ex) {
                JOptionPane.showMessageDialog(rootPane, "No s'ha pogut mostrar el contingut del Album "+titol+" : "+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
            llistaContingutAlbum.setModel(model);
        } else {
            
        }
