    private void btnReproFitxerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReproFitxerActionPerformed
        if (controlador.getBiblio().isEmpty())  {
            JOptionPane.showMessageDialog(rootPane, "Afegeix algun fitxer abans de reproduir-lo","Error",JOptionPane.ERROR_MESSAGE);
        } else {
            if(llistaBiblioteca.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Selecciona un fitxer abans de reproduir-lo","Atenció",JOptionPane.INFORMATION_MESSAGE);
            } else {
                int id = llistaBiblioteca.getSelectedIndex()+1;
                try {
                    controlador.reproduirFitxer(id);
                    panellGestioReproduccio.setVisible(true);
                } catch (AplicacioException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Error al intentar reproduir el fitxer: "+ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnReproFitxerActionPerformed

