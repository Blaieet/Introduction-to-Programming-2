    private void omplirLlista() {
        DefaultListModel model = new DefaultListModel();
        model.clear();
        for (FitxerMultimedia item: controlador.getBiblio().getLlista()){
            model.addElement(item);
        }
        llistaBiblio.setModel(model);
    
    }
    
    private void btnAfegirAlbumFitxersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAfegirAlbumFitxersActionPerformed
        if (llistaBiblio.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Selecciona un fitxer a afegir", "Atencio", JOptionPane.WARNING_MESSAGE);
        } else {
            for (Object item: llistaBiblio.getSelectedValues()) {
                FitxerMultimedia fitxer = (FitxerMultimedia) item;
                try {
                    albumaAfegir.addFitxer(fitxer);
                    this.dispose();
                } catch (AplicacioException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Error al intentar afegir un fitxer: "+ex.getMessage(),"Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnAfegirAlbumFitxersActionPerformed

